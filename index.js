let currentLang = "it"; // Default language

// DOM Elements Translation
function translatePage(lang) {
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach(el => {
    const key = el.getAttribute("data-translate");
    if (translations[lang] && translations[lang][key]) {
      // Use innerHTML to support standard italic tag styling
      el.innerHTML = translations[lang][key];
    }
  });

  // Handle placeholders
  const inputs = document.querySelectorAll("[data-translate-placeholder]");
  inputs.forEach(input => {
    const key = input.getAttribute("data-translate-placeholder");
    if (translations[lang] && translations[lang][key]) {
      input.setAttribute("placeholder", translations[lang][key]);
    }
  });
}

// Set up event listeners
document.addEventListener("DOMContentLoaded", () => {
  // Pre-load default language
  translatePage(currentLang);

  // Initialize GSAP Loader and Scroll Animations
  initAnimations();

  // Language buttons toggle
  const langBtns = document.querySelectorAll(".lang-btn");
  langBtns.forEach(btn => {
    btn.addEventListener("click", () => {
      const lang = btn.getAttribute("data-lang");
      if (lang === currentLang) return;

      currentLang = lang;
      langBtns.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");

      // Animate transition between languages
      gsap.to("body", {
        opacity: 0.1,
        duration: 0.25,
        onComplete: () => {
          translatePage(currentLang);
          gsap.to("body", { opacity: 1, duration: 0.35 });
        }
      });
    });
  });

  // Mobile menu triggers
  const burgerWrap = document.querySelector(".menu-burger_wrap");
  const burger = document.querySelector(".burger_wrap");
  const mobileMenu = document.querySelector(".menu-mobile");
  const mobileLinks = document.querySelectorAll(".mobile_nav-link");

  function toggleMobileMenu() {
    const isActive = mobileMenu.classList.contains("active");
    
    if (!isActive) {
      // Open
      mobileMenu.style.display = "flex";
      // Force repaint
      mobileMenu.offsetHeight;
      mobileMenu.classList.add("active");
      burger.classList.add("active");
      document.body.style.overflow = "hidden"; // Lock page scroll

      // Animate links slide up
      gsap.fromTo(".mobile_nav-link", 
        { y: 50, opacity: 0 }, 
        { y: 0, opacity: 1, duration: 0.8, stagger: 0.1, ease: "power3.out", delay: 0.2 }
      );
    } else {
      // Close
      mobileMenu.classList.remove("active");
      burger.classList.remove("active");
      document.body.style.overflow = ""; // Restore scroll

      setTimeout(() => {
        mobileMenu.style.display = "none";
      }, 800); // Match CSS transition length
    }
  }

  burgerWrap.addEventListener("click", toggleMobileMenu);

  mobileLinks.forEach(link => {
    link.addEventListener("click", () => {
      // Close menu and navigate
      toggleMobileMenu();
    });
  });

  // Header scroll appearance
  window.addEventListener("scroll", () => {
    const nav = document.querySelector(".nav_fixed");
    if (window.scrollY > 50) {
      nav.classList.add("scrolled");
    } else {
      nav.classList.remove("scrolled");
    }
  });

  // Form submission handler
  const contactForm = document.getElementById("bookingForm");
  if (contactForm) {
    contactForm.addEventListener("submit", (e) => {
      e.preventDefault();
      
      const submitBtn = contactForm.querySelector("button[type='submit']");
      const originalText = submitBtn.innerHTML;
      submitBtn.disabled = true;
      submitBtn.innerHTML = currentLang === "it" ? "Invio in corso..." : "Sending...";

      setTimeout(() => {
        // Mock success
        alert(currentLang === "it" ? "Grazie! Il messaggio è stato inviato correttamente." : "Thank you! Your request has been successfully submitted.");
        contactForm.reset();
        submitBtn.disabled = false;
        submitBtn.innerHTML = originalText;
      }, 1500);
    });
  }
});

// GSAP + ScrollTrigger Setup
function initAnimations() {
  // Ensure GSAP is loaded
  if (typeof gsap === "undefined") {
    console.warn("GSAP is not loaded. Skipping animations.");
    // Hide loader immediately if GSAP fails
    const loader = document.querySelector(".loader");
    if (loader) loader.style.display = "none";
    return;
  }

  // Register ScrollTrigger plugin
  if (typeof ScrollTrigger !== "undefined") {
    gsap.registerPlugin(ScrollTrigger);
  }

  // Disable default scroll restoration
  if ('scrollRestoration' in history) {
    history.scrollRestoration = 'manual';
  }
  window.scrollTo(0, 0);

  // 1. PRELOADER TIMELINE
  const tlLoader = gsap.timeline({
    onComplete: () => {
      // Remove loader from DOM or set display none
      document.querySelector(".loader").style.display = "none";
      document.body.style.overflow = ""; // Enable scrolling
      
      // Trigger Hero Animation
      triggerHeroAnimation();
    }
  });

  document.body.style.overflow = "hidden"; // Lock scroll initially

  // Loader Text Reveal
  tlLoader.to(".loader_heading", {
    opacity: 1,
    y: 0,
    duration: 1.2,
    ease: "power3.out",
    delay: 0.3
  })
  // Clip-path Reveal of the Image Mask (opens from center)
  .to(".loader-mask", {
    clipPath: "inset(0% 0% 0% 0%)",
    duration: 2.0,
    ease: "power4.inOut"
  }, "-=0.6")
  // Parallel Image zoom-out effect
  .to(".loader-img", {
    scale: 1,
    duration: 2.2,
    ease: "power3.out"
  }, "-=2.0")
  // Fade out text
  .to(".loader_heading", {
    opacity: 0,
    y: -30,
    duration: 0.8,
    ease: "power3.in"
  }, "-=0.8")
  // Slide entire loader panel up
  .to(".loader", {
    yPercent: -100,
    duration: 1.2,
    ease: "power4.inOut"
  }, "-=0.3");

  // 2. HERO ANIMATION - Simplified to prevent delayed animation per user request
  function triggerHeroAnimation() {
    // Elements remain static and instantly readable as the loader exits
  }

  // 3. SCROLL-TRIGGERED PARALLAX AND REVEALS
  if (typeof ScrollTrigger !== "undefined") {
    // Parallax on Banner Quote background
    gsap.fromTo(".quote_bg-img", 
      { yPercent: -15 },
      { 
        yPercent: 15,
        ease: "none",
        scrollTrigger: {
          trigger: ".section_quote",
          start: "top bottom",
          end: "bottom top",
          scrub: true
        }
      }
    );

    // Parallax on hero background (subtle scrolling)
    gsap.fromTo(".hero_bg-img", 
      { yPercent: 0 },
      { 
        yPercent: 12,
        ease: "none",
        scrollTrigger: {
          trigger: ".section_hero",
          start: "top top",
          end: "bottom top",
          scrub: true
        }
      }
    );

    // Fade reveal for spaces cards
    const spaceCards = document.querySelectorAll(".spazi_card");
    spaceCards.forEach(card => {
      const img = card.querySelector(".spazi_img");
      const textElements = card.querySelectorAll(".spazi_card-title, .spazi_card-desc, .spazi_card-meta");
      
      gsap.fromTo(img, 
        { scale: 1.15, clipPath: "inset(10% 0 10% 0)" },
        { 
          scale: 1, 
          clipPath: "inset(0% 0 0% 0)", 
          duration: 1.4, 
          ease: "power3.out",
          scrollTrigger: {
            trigger: card,
            start: "top 85%"
          }
        }
      );

      gsap.fromTo(textElements,
        { y: 30, opacity: 0 },
        {
          y: 0,
          opacity: 1,
          duration: 1.0,
          stagger: 0.15,
          ease: "power3.out",
          scrollTrigger: {
            trigger: card,
            start: "top 80%"
          }
        }
      );
    });

    // Fade reveal for Guest Service cards
    gsap.fromTo(".service_card",
      { y: 50, opacity: 0 },
      {
        y: 0,
        opacity: 1,
        duration: 1.0,
        stagger: 0.2,
        ease: "power3.out",
        scrollTrigger: {
          trigger: ".services_grid",
          start: "top 85%"
        }
      }
    );

    // Fade reveal for Gallery images
    const galleryItems = document.querySelectorAll(".gallery_item");
    galleryItems.forEach(item => {
      gsap.fromTo(item,
        { y: 60, opacity: 0 },
        {
          y: 0,
          opacity: 1,
          duration: 1.2,
          ease: "power3.out",
          scrollTrigger: {
            trigger: item,
            start: "top 90%"
          }
        }
      );
    });

    // Fade reveal for Contact sections
    gsap.fromTo(".contact_form-panel",
      { x: -50, opacity: 0 },
      {
        x: 0,
        opacity: 1,
        duration: 1.2,
        ease: "power3.out",
        scrollTrigger: {
          trigger: ".section_contact",
          start: "top 80%"
        }
      }
    );

    gsap.fromTo(".contact_info-panel",
      { x: 50, opacity: 0 },
      {
        x: 0,
        opacity: 1,
        duration: 1.2,
        ease: "power3.out",
        scrollTrigger: {
          trigger: ".section_contact",
          start: "top 80%"
        }
      }
    );
  }
}
